
public class ciclosfor {

	public static void main(String[] args) {
		//variable			condicion	ejecutar al final
		for (int contador=0;contador<=10; contador++) {
			for (int multipo=0;multipo<=10; multipo++) {
				System.out.print(multipo * contador);
				System.out.print(" ");
			}	
		System.out.println();

		}
		}

}
